calc1 = int(input("\n\nInsira um numero inteiro a ser calculado: "))
calc2 = int(input("\n\nInsira um numero inteiro a ser calculado: "))

def mult(calc1,calc2):
    return calc1 * calc2

result = mult(calc1,calc2)
print (f"\n\nA multiplicação de um pelo outro é: {result}")


def div(calc1,calc2):
    return calc1 / calc2

result = div(calc1,calc2)
print (f"\n\nA Divisão de um pelo outro é: {result}")


def div(calc1,calc2):
    return calc1 / 2, calc2 / 2

result = div(calc1,calc2)
print (f"\n\nA Metade dos 2 números é: {result}")


